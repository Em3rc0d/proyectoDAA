    private void llenarDatos(Paciente paciente){
        txtNombre.setText(paciente.nombre);
        txtApellido.setText(paciente.apellido);
        txtNumTelf.setText(""+paciente.getNumTelefono());
        txtContacEmerg.setText(""+paciente.getHistorialMedico().getContactoEmergencia());
        txtAntecMedic.setText(paciente.getHistorialMedico().getAntecMedicos());
        txtAlergias.setText(paciente.getHistorialMedico().getAlergias());
        txtMedicamentos.setText(paciente.getHistorialMedico().getMedicamentos());
        txtTratAnteriores.setText(paciente.getHistorialMedico().getTratamientosAnteriores());
    }
    
